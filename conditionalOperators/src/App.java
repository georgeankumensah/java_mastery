public class App {
    public static void main(String[] args) throws Exception {
        
        boolean bool1 = true;
        boolean bool2 = false;
        boolean result;


        result = bool1 && bool2;
        System.out.println(result);

        result = bool1 || bool2;
        System.out.println(result);

        result = !bool1;
        System.out.println(result);


        
    }
}
